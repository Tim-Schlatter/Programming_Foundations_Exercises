# Programming Exercise 5-13: Kinetic Energy Calculator
#
# Task: Write a program that calculates kinetic energy using functions.
#
# Requirements:
# 1. Create a main function that handles user input and calls calculation function
# 2. Create a kinetic_energy function that calculates kinetic energy
# 3. Ask the user to enter mass and velocity
# 4. Calculate and display kinetic energy in joules
#
# Functions:
# - main(): handles input and calls kinetic_energy()
# - kinetic_energy(mass, velocity): calculates kinetic energy
#
# Physics Formula:
# - Kinetic Energy = (mass × velocity²) / 2
# - Where mass is in kilograms and velocity is in meters per second
#
# Logic:
# - Get mass and velocity from user
# - Pass values to kinetic_energy function
# - Calculate KE = (mass * velocity * velocity) / 2
# - Return kinetic energy value
# - Display result formatted to 2 decimal places
#
# Example:
# Enter the mass of the object in kilograms: 10
# Enter object velocity in meters per second: 5
# Kinetic Energy is: 125.00 joules
