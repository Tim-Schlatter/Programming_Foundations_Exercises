# 6. Display tax breakdown
#
# Constants:
# - STATE_TAX_RATE = 0.05 (5%)
# - COUNTY_TAX_RATE = 0.025 (2.5%)
#
# Logic:
# - Get total sales from user
# - Calculate state tax = sales * 0.05
# - Calculate county tax = sales * 0.025
# - Calculate total tax = state tax + county tax
# - Display formatted currency output for each tax type
#
# Example:
# Enter the total sales for the month: 10000
# State Tax: $500.00
# County Tax: $250.00
