# Programming Exercise 3-15: Leap Year Calculator
#
# Task: Write a program that determines if a year is a leap year and how many days February has.
#
# Requirements:
# 1. Ask the user to enter a year
# 2. Determine if it's a leap year using the rules:
#    - If year is divisible by 100: must also be divisible by 400
#    - If year is not divisible by 100: must be divisible by 4
# 3. Display whether it's a leap year and the number of days in February
#
# Leap Year Rules:
# - Year divisible by 4 is a leap year (except century years)
# - Century years (divisible by 100) are leap years only if divisible by 400
#
# Example:
# Enter the year: 2020
# That is a leap year. February has 29 days.
#
# Enter the year: 2021
# That is not a leap year. February has 28 days.
#
# Enter the year: 2000
# That is a leap year. February has 29 days.
